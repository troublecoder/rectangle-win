/**
 * Catppuccin 색상을 Nuxt UI v4의 11단계(50-950) 스케일로 변환.
 *
 * 전략:
 * - neutral: Latte(50-500) + Mocha(600-950) surface 톤 직접 매핑 (단조 감소)
 * - accent(primary/mauve, error/red, warning/peach, success/green, info/blue):
 *   Catppuccin oklch 값 기반 보간으로 50-950 생성
 *
 * 출력: src/assets/css/_catppuccin.css (@theme static 블록)
 */
const palette = require('@catppuccin/palette').flavors
const fs = require('fs')
const path = require('path')

const latte = palette.latte.colors
const mocha = palette.mocha.colors

// ── neutral: Latte + Mocha surface 톤 단조 감소 스케일 ──────────────
// 라이트 모드는 50-300을 bg/chrome으로 사용, 다크 모드는 800-950 사용.
const neutralScale = {
  50:  latte.base.hex,      // #eff1f5  — light bg
  100: latte.mantle.hex,    // #e6e9ef
  200: latte.crust.hex,     // #dce0e8
  300: latte.surface0.hex,  // #ccd0da
  400: latte.surface1.hex,  // #bcc0cc
  500: latte.surface2.hex,  // #acb0be  — 중간점
  600: mocha.surface2.hex,  // #585b70
  700: mocha.surface1.hex,  // #45475a
  800: mocha.surface0.hex,  // #313244
  900: mocha.mantle.hex,    // #181825  — dark bg
  950: mocha.crust.hex,     // #11111b  — darkest
}

// ── oklch 보간으로 accent 스케일 생성 ──────────────────────────────
function lerp(a, b, t) { return a + (b - a) * t }
function lerpAngle(a, b, t) {
  let diff = b - a
  if (diff > 180) diff -= 360
  if (diff < -180) diff += 360
  return (a + diff * t + 360) % 360
}

/**
 * anchorColor(L~500)에서 50(밝음)~950(어두움) 스케일 생성.
 * Catppuccin oklch 값 사용으로 지각적 균등 보간.
 */
function generateScale(anchor, hueShift = 0) {
  const { l: al, c: ac, h: ah } = anchor.oklch
  const shades = {}
  // L(명도) 매핑: 50=0.97 ~ 500=anchor ~ 950=0.15
  const lightnessTargets = {
    50: 0.97, 100: 0.94, 200: 0.89, 300: 0.83, 400: 0.76,
    500: al,
    600: 0.58, 700: 0.48, 800: 0.38, 900: 0.28, 950: 0.18,
  }
  // chroma(채도)는 anchor 기준 양끝에서 약간 감소
  for (const [shade, targetL] of Object.entries(lightnessTargets)) {
    const t = (Number(shade) - 50) / 900  // 0..1
    const c = ac * (0.45 + 0.55 * (1 - Math.abs(t - 0.5) * 1.2))
    const h = ah + hueShift
    shades[shade] = oklchToHex(targetL, Math.max(c, 0.02), h)
  }
  return shades
}

function oklchToHex(l, c, h) {
  // oklch → linear sRGB → gamma → hex
  const hr = (h * Math.PI) / 180
  const a = c * Math.cos(hr)
  const b = c * Math.sin(hr)
  // OKLab → linear RGB
  const l_ = l + 0.3963377774 * a + 0.2158037573 * b
  const m_ = l - 0.1055613458 * a - 0.0638541728 * b
  const s_ = l - 0.0894841775 * a - 1.291485548 * b
  const l3 = l_ ** 3
  const m3 = m_ ** 3
  const s3 = s_ ** 3
  let r = +4.0767416621 * l3 - 3.3077115913 * m3 + 0.2309699292 * s3
  let g = -1.2684380046 * l3 + 2.6097574011 * m3 - 0.3413193965 * s3
  let bl = -0.0041960863 * l3 - 0.7034186147 * m3 + 1.707614701 * s3
  // linear → sRGB gamma
  r = linearToGamma(r); g = linearToGamma(g); bl = linearToGamma(bl)
  return rgbToHex(r, g, bl)
}
function linearToGamma(x) {
  return x >= 0.0031308 ? 1.055 * Math.pow(x, 1 / 2.4) - 0.055 : 12.92 * x
}
function rgbToHex(r, g, b) {
  const clamp = (v) => Math.max(0, Math.min(1, v))
  const to255 = (v) => Math.round(clamp(v) * 255)
  return '#' + [to255(r), to255(g), to255(b)].map(x => x.toString(16).padStart(2, '0')).join('')
}

// Catppuccin Mocha accent 색상 기준 스케일 생성
const primary  = generateScale(mocha.mauve)    // Mauve
const error    = generateScale(mocha.red)      // Red
const warning  = generateScale(mocha.peach)    // Peach
const success  = generateScale(mocha.green)    // Green
const info     = generateScale(mocha.blue)     // Blue

// ── CSS 출력 ────────────────────────────────────────────────────────
function colorBlock(name, scale) {
  let css = ''
  for (const [shade, hex] of Object.entries(scale)) {
    css += `  --color-${name}-${shade}: ${hex};\n`
  }
  return css
}

const css = `/* AUTO-GENERATED by scripts/gen-catppuccin-theme.js — DO NOT EDIT */
@theme static {
  /* ── Neutral: Latte(light 50-500) + Mocha(dark 600-950) ── */
${colorBlock('neutral', neutralScale)}
  /* ── Primary: Catppuccin Mauve ── */
${colorBlock('primary', primary)}
  /* ── Error: Catppuccin Red ── */
${colorBlock('error', error)}
  /* ── Warning: Catppuccin Peach ── */
${colorBlock('warning', warning)}
  /* ── Success: Catppuccin Green ── */
${colorBlock('success', success)}
  /* ── Info: Catppuccin Blue ── */
${colorBlock('info', info)}
}
`

const outDir = path.resolve(__dirname, '..', 'src', 'assets', 'css')
fs.writeFileSync(path.join(outDir, '_catppuccin.css'), css)
console.log('✓ Generated src/assets/css/_catppuccin.css')

// 검증용 미리보기
console.log('\n=== Generated Scales (500 anchor) ===')
for (const [name, scale] of Object.entries({ neutral: neutralScale, primary, error, warning, success, info })) {
  console.log(`${name.padEnd(8)} 500=${scale[500]}  50=${scale[50]}  950=${scale[950]}`)
}
